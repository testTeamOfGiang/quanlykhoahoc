package entity;

public class Phonghoc {

	private int id_PH;
	private String ghichu_PH;
	private int sucChua_PH;
	private String ten_PH;

	public Phonghoc() {
	}

	public int getId_PH() {
		return this.id_PH;
	}

	public void setId_PH(int id_PH) {
		this.id_PH = id_PH;
	}

	public String getGhichu_PH() {
		return this.ghichu_PH;
	}

	public void setGhichu_PH(String ghichu_PH) {
		this.ghichu_PH = ghichu_PH;
	}

	public String getTen_PH() {
		return this.ten_PH;
	}

	public void setTen_PH(String ten_PH) {
		this.ten_PH = ten_PH;
	}

	public int getSucChua_PH() {
		return sucChua_PH;
	}

	public void setSucChua_PH(int sucChua_PH) {
		this.sucChua_PH = sucChua_PH;
	}
	
	

}