package entity;

public class Khoahoc {

	private int id_KH;

	private String ghichu_KH;

	private int gia_KH;

	private String ten_KH;

	public Khoahoc() {
	}

	public int getId_KH() {
		return this.id_KH;
	}

	public void setId_KH(int id_KH) {
		this.id_KH = id_KH;
	}

	public String getGhichu_KH() {
		return this.ghichu_KH;
	}

	public void setGhichu_KH(String ghichu_KH) {
		this.ghichu_KH = ghichu_KH;
	}

	public int getGia_KH() {
		return this.gia_KH;
	}

	public void setGia_KH(int gia_KH) {
		this.gia_KH = gia_KH;
	}

	public String getTen_KH() {
		return this.ten_KH;
	}

	public void setTen_KH(String ten_KH) {
		this.ten_KH = ten_KH;
	}

}